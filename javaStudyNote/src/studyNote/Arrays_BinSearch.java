import java.util.Arrays;

public class Arrays_BinSearch {

	public static void main(String[] args) {

		int x[] = {1,2,3,4,5,6,7,8,9};
	
		int key = 11; 
		
		
		if(Arrays.binarySearch(x, key) < 0)
			System.out.println("찾는 값이 없습니다.");
		else
		System.out.println(Arrays.binarySearch(x, key));
		
		
		
		
	}

}
